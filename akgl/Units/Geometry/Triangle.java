package akgl.Units.Geometry;

/**
 *
 * @author Robert Kollar
 */
public class Triangle {

    private int A, B, C;

    public Triangle(int A, int B, int C) {
        this.A = A;
        this.B = B;
        this.C = C;
    }

    public int getA() {
        return A;
    }

    public int getB() {
        return B;
    }

    public int getC() {
        return C;
    }

}
